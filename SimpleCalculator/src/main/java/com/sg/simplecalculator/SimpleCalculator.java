package com.sg.simplecalculator;

/**
 *
 * @author apprentice
 */
public class SimpleCalculator {

    public static double multiplication(int numb1, int numb2) {
        return numb1 * numb2;
    }

    public static double division(int numb1, int numb2) {
        return numb1 / numb2;
    }

    public static double addition(int numb1, int numb2) {
        return numb1 + numb2;
    }

    public static double subtraction(int numb1, int numb2) {
        return numb1 - numb2;
    }
}
