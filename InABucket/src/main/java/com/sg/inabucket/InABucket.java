public class InABucket {
    
    public static void main(String[] args) {
    
    // You can declare all KINDS of variables.
        String walrus;
        double piesEaten;
        float weightOfTeacupPig;
        int grainsOfSand;

        // But declaring them just sets up the space for data
        // to use the variable, you have to put data IN it first!
        walrus = "Sir Leroy Jenkins III";
        piesEaten = 42.1;
        weightOfTeacupPig = 11;
        grainsOfSand = 121;
        System.out.println("Meet my pet Walrus, " + walrus);
        System.out.println("He was a bit hungry today, and ate this many pies : " + piesEaten);
        //System.out.println("he ate " + piesEaten);
        System.out.println("the teacup weighed " + weightOfTeacupPig);
        System.out.println("and inside the teacup there were" + grainsOfSand + "grains");
    }
}

    
    
    
